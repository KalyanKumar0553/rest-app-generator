	private boolean supportsJavaRecords(Object javaVersion) {
		String raw = trimmed(javaVersion);
		if (raw.isBlank()) {
			return true;
		}
		String normalized = raw.startsWith("1.") ? raw.substring(2) : raw;
		Matcher matcher = CONSTRAINT_PARAM_PATTERN.matcher(normalized);
		if (!matcher.find()) {
			return true;
		}
		try {
			return Integer.parseInt(matcher.group()) >= 17;
		} catch (NumberFormatException ex) {
			return true;
		}
	}

	private Map<String, Object> mapRelation(Map<String, Object> relation) {
		String type = defaultIfBlank(trimmed(relation.get("relationType")), "ManyToOne");
		Map<String, Object> mapped = linkedMap();
		mapped.put("name", defaultIfBlank(trimmed(relation.get("sourceFieldName")), "relation"));
		mapped.put("type", type);
		mapped.put("target", defaultIfBlank(trimmed(relation.get("targetEntity")), "Target"));

		List<String> cascade = asStringList(relation.get("cascade"));
		if (!cascade.isEmpty()) {
			mapped.put("cascade", new ArrayList<>(new LinkedHashSet<>(cascade)));
		}
		String mappedBy = trimmed(relation.get("mappedBy"));
		if (!mappedBy.isBlank()) {
			mapped.put("mappedBy", mappedBy);
		}
		if (relation.get("orphanRemoval") == Boolean.TRUE) {
			mapped.put("orphanRemoval", true);
		}
		String orderBy = trimmed(relation.get("orderBy"));
		if (!orderBy.isBlank()) {
			mapped.put("orderBy", orderBy);
		}
		String orderColumnName = trimmed(getNested(relation, "orderColumn", "name"));
		if (!orderColumnName.isBlank()) {
			mapped.put("orderColumn", mapOf("name", orderColumnName));
		}
		if (List.of("ManyToOne", "OneToOne").contains(type)) {
			if (relation.get("optional") instanceof Boolean optional) {
				mapped.put("optional", optional);
			} else if (relation.get("required") instanceof Boolean required) {
				mapped.put("optional", !required);
			}
			Map<String, Object> joinColumn = mapJoinColumn(asMap(relation.get("joinColumn")));
			if (joinColumn != null) {
				mapped.put("joinColumn", joinColumn);
			}
		}
		if ("ManyToMany".equals(type)) {
			Map<String, Object> joinTable = mapJoinTable(asMap(relation.get("joinTable")));
			if (joinTable != null) {
				mapped.put("joinTable", joinTable);
			}
		}
		return mapped;
	}

	private Map<String, Object> mapJoinColumn(Map<String, Object> joinColumn) {
		if (joinColumn.isEmpty()) {
			return null;
		}
		Map<String, Object> mapped = linkedMap();
		String name = trimmed(joinColumn.get("name"));
		String referenced = trimmed(joinColumn.get("referencedColumnName"));
		if (!name.isBlank()) {
			mapped.put("name", name);
		}
		if (!referenced.isBlank()) {
			mapped.put("referencedColumnName", referenced);
		}
		if (joinColumn.get("nullable") instanceof Boolean nullable) {
			mapped.put("nullable", nullable);
		}
		if (joinColumn.get("index") == Boolean.TRUE) {
			mapped.put("index", true);
		}
		String onDelete = trimmed(joinColumn.get("onDelete"));
		if (!onDelete.isBlank()) {
			mapped.put("onDelete", onDelete);
		}
		return mapped.isEmpty() ? null : mapped;
	}

	private Map<String, Object> mapJoinTable(Map<String, Object> joinTable) {
		if (joinTable.isEmpty()) {
			return null;
		}
		Map<String, Object> mapped = linkedMap();
		String name = trimmed(joinTable.get("name"));
		if (!name.isBlank()) {
			mapped.put("name", name);
		}
		List<Map<String, Object>> joinColumns = mapJoinColumns(joinTable.get("joinColumns"));
		if (!joinColumns.isEmpty()) {
			mapped.put("joinColumns", joinColumns);
		}
		List<Map<String, Object>> inverseJoinColumns = mapJoinColumns(joinTable.get("inverseJoinColumns"));
		if (!inverseJoinColumns.isEmpty()) {
			mapped.put("inverseJoinColumns", inverseJoinColumns);
		}
		if (joinTable.get("uniquePair") == Boolean.TRUE) {
			mapped.put("uniquePair", true);
		}
		String onDelete = trimmed(joinTable.get("onDelete"));
		if (!onDelete.isBlank()) {
			mapped.put("onDelete", onDelete);
		}
		return mapped.isEmpty() ? null : mapped;
	}

	private List<Map<String, Object>> mapJoinColumns(Object columnsRaw) {
		List<Map<String, Object>> result = new ArrayList<>();
		for (Object raw : asList(columnsRaw)) {
			Map<String, Object> mapped = mapJoinColumn(asMap(raw));
			if (mapped != null) {
				result.add(mapped);
			}
		}
		return result;
	}

	private List<Object> mapConstraints(Object constraintsRaw) {
		List<Object> result = new ArrayList<>();
		for (Object raw : asList(constraintsRaw)) {
			Object mapped = mapConstraint(asMap(raw));
			if (mapped != null) {
				result.add(mapped);
			}
		}
		return result;
	}

	private Object mapConstraint(Map<String, Object> constraint) {
		String name = trimmed(constraint.get("name"));
		if (name.isBlank()) {
			return null;
		}
		String value = trimmed(constraint.get("value"));
		String value2 = trimmed(constraint.get("value2"));
		Map<String, Object> params = linkedMap();
		switch (name) {
			case "Size":
				if (!value.isBlank()) {
					params.put("min", toNumberIfPossible(value));
				}
				if (!value2.isBlank()) {
					params.put("max", toNumberIfPossible(value2));
				}
				break;
			case "Min":
			case "Max":
				if (!value.isBlank()) {
					params.put("value", toNumberIfPossible(value));
				}
				break;
			case "DecimalMin":
			case "DecimalMax":
				if (!value.isBlank()) {
					params.put("value", value);
				}
				if (!value2.isBlank()) {
					params.put("inclusive", toBooleanIfPossible(value2));
				}
				break;
			case "Digits":
				if (!value.isBlank()) {
					params.put("integer", toNumberIfPossible(value));
				}
				if (!value2.isBlank()) {
					params.put("fraction", toNumberIfPossible(value2));
				}
				break;
			case "Pattern":
				if (!value.isBlank()) {
					params.put("regex", value);
				}
				break;
			default:
				if (!value.isBlank()) {
					params.put("value", toNumberIfPossible(value));
				}
				if (!value2.isBlank()) {
					params.put("value2", toNumberIfPossible(value2));
				}
		}
		if (params.isEmpty()) {
			return name;
		}
		return mapOf(name, params);
	}

	private Map<String, Object> mapId(Map<String, Object> primaryKeyField) {
		if (primaryKeyField == null || primaryKeyField.isEmpty()) {
			return mapOf(
					"field", "id",
					"type", "Long",
					"generation", mapOf("strategy", "IDENTITY"));
		}
		String type = normalizeType(primaryKeyField.get("type"));
		Map<String, Object> generation = "UUID".equals(type)
				? mapOf("strategy", "UUID", "generatorName", "uuid", "hibernateUuidStrategy", "uuid2")
				: mapOf("strategy", "IDENTITY");
		return mapOf(
				"field", defaultIfBlank(trimmed(primaryKeyField.get("name")), "id"),
				"type", type,
				"generation", generation);
	}


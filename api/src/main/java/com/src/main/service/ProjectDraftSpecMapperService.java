	private List<Map<String, Object>> mapModels(List<Object> entities, List<Object> relations, Map<String, String> entityToSpecName) {
		List<Map<String, Object>> models = new ArrayList<>();
		for (Object entityRaw : entities) {
			Map<String, Object> entity = asMap(entityRaw);
			List<Object> fields = asList(entity.get("fields"));
			Map<String, Object> idField = null;
			List<Object> nonIdFields = new ArrayList<>();
			for (Object fieldRaw : fields) {
				Map<String, Object> field = asMap(fieldRaw);
				if (asBoolean(field.get("primaryKey"))) {
					idField = field;
				} else {
					nonIdFields.add(field);
				}
			}

			List<Map<String, Object>> modelRelations = new ArrayList<>();
			String entityName = trimmed(entity.get("name"));
			for (Object relationRaw : relations) {
				Map<String, Object> relation = asMap(relationRaw);
				if (trimmed(relation.get("sourceEntity")).equals(entityName)) {
					modelRelations.add(mapRelation(relation));
				}
			}

			Map<String, Object> model = linkedMap();
			model.put("name", entityName.isBlank() ? "Entity" : entityName);
			model.put("tableName", toSnakeCase(entityName.isBlank() ? "entity" : entityName));
			model.put("addRestEndpoints", asBoolean(entity.get("addRestEndpoints")));
			model.put("addCrudOperations", asBoolean(entity.get("addCrudOperations")));
			model.put("classMethods", mapClassMethods(entity.get("classMethods")));

			Map<String, Object> options = linkedMap();
			options.put("entity", !asBoolean(entity.get("mappedSuperclass")));
			options.put("immutable", asBoolean(entity.get("immutable")));
			options.put("auditing", asBoolean(entity.get("auditable")));
			options.put("softDelete", asBoolean(entity.get("softDelete")));
			options.put("naturalIdCache", asBoolean(entity.get("naturalIdCache")));
			model.put("options", options);
			model.put("id", mapId(idField));

			List<Map<String, Object>> mappedFields = new ArrayList<>();
			for (Object field : nonIdFields) {
				mappedFields.add(mapModelField(asMap(field)));
			}
			model.put("fields", mappedFields);

			String mappedSpecName = entityToSpecName.get(entityName);
			if (mappedSpecName != null && !mappedSpecName.isBlank()) {
				model.put("rest-spec-name", mappedSpecName);
			}

			Map<String, Object> restConfig = mapRestConfig(asMap(entity.get("restConfig")));
			if (restConfig != null) {
				model.put("rest", restConfig);
			}
			if (!modelRelations.isEmpty()) {
				model.put("relations", modelRelations);
			}

			models.add(model);
		}
		return models;
	}

	private RestSpecSection buildRestSpecSection(List<Object> entities, Map<String, Object> controllersConfig, boolean includeControllers) {
		List<Map<String, Object>> specs = new ArrayList<>();
		Map<String, String> entityToSpecName = new LinkedHashMap<>();
		Map<String, Integer> specIndexByName = new LinkedHashMap<>();

		for (Object entityRaw : entities) {
			Map<String, Object> entity = asMap(entityRaw);
			if (!asBoolean(entity.get("addRestEndpoints"))) {
				continue;
			}
			Map<String, Object> mapped = mapRestConfig(asMap(entity.get("restConfig")));
			if (mapped == null) {
				continue;
			}
			String entityName = trimmed(entity.get("name"));
			String specName = addOrUpdateSpec(specs, specIndexByName,
					defaultIfBlank(trimmed(mapped.get("resourceName")), entityName.isBlank() ? "EntityRest" : entityName), mapped);
			if (!entityName.isBlank()) {
				entityToSpecName.put(entityName, specName);
			}
		}

		if (includeControllers) {
			Map<String, Object> mappedControllers = mapRestConfig(controllersConfig);
			if (mappedControllers != null) {
				addOrUpdateSpec(specs, specIndexByName,
						defaultIfBlank(trimmed(mappedControllers.get("resourceName")), "ControllersRest"),
						mappedControllers);
			}
		}

		return new RestSpecSection(specs, entityToSpecName);
	}

	private String addOrUpdateSpec(List<Map<String, Object>> specs, Map<String, Integer> specIndexByName, String name,
			Map<String, Object> config) {
		String specName = defaultIfBlank(trimmed(name), "RestSpec");
		Map<String, Object> spec = linkedMap();
		spec.put("name", specName);
		spec.putAll(config);
		Integer existingIndex = specIndexByName.get(specName);
		if (existingIndex == null) {
			specIndexByName.put(specName, specs.size());
			specs.add(spec);
		} else {
			specs.set(existingIndex, spec);
		}
		return specName;
	}

	private boolean hasNamedControllersConfig(Map<String, Object> config) {
		return !trimmed(config.get("resourceName")).isBlank();
	}

	private Map<String, Object> mapRestConfig(Map<String, Object> restConfig) {
		if (restConfig.isEmpty()) {
			return null;
		}
		List<String> operations = List.of("list", "get", "create", "patch", "delete", "bulkInsert", "bulkUpdate", "bulkDelete");
		Map<String, Object> methods = linkedMap();
		for (String key : operations) {
			if (!asBoolean(getNested(restConfig, "methods", key))) {
				continue;
			}
			Map<String, Object> method = linkedMap();
			method.put("request", buildOperationRequestConfig(key, restConfig));
			method.put("response", buildOperationResponseConfig(restConfig, key));
			Map<String, Object> documentation = buildOperationDocumentationConfig(restConfig, key);
			if (documentation != null) {
				method.put("documentation", documentation);
			}
			methods.put(key, method);
		}

		Map<String, Object> mapped = linkedMap();
		mapped.put("resourceName", trimmed(restConfig.get("resourceName")));
		mapped.put("basePath", trimmed(restConfig.get("basePath")));

		Map<String, Object> apiVersioning = linkedMap();
		apiVersioning.put("enabled", asBoolean(getNested(restConfig, "apiVersioning", "enabled")));
		apiVersioning.put("strategy", "path".equals(trimmed(getNested(restConfig, "apiVersioning", "strategy"))) ? "path" : "header");
		apiVersioning.put("headerName", trimmed(getNested(restConfig, "apiVersioning", "headerName")));
		apiVersioning.put("defaultVersion", trimmed(getNested(restConfig, "apiVersioning", "defaultVersion")));
		mapped.put("apiVersioning", apiVersioning);

		String pathVariableType = String.valueOf(restConfig.getOrDefault("pathVariableType", "UUID"));
		mapped.put("pathVariableType", List.of("UUID", "LONG", "STRING").contains(pathVariableType) ? pathVariableType : "UUID");

		Map<String, Object> deletion = linkedMap();
		deletion.put("mode", "HARD".equals(restConfigValue(restConfig, "deletion", "mode")) ? "HARD" : "SOFT");
		deletion.put("restoreEndpoint", asBoolean(getNested(restConfig, "deletion", "restoreEndpoint")));
		deletion.put("includeDeletedParam", asBoolean(getNested(restConfig, "deletion", "includeDeletedParam")));
		mapped.put("deletion", deletion);

		Map<String, Object> hateoas = linkedMap();
		hateoas.put("enabled", asBoolean(getNested(restConfig, "hateoas", "enabled")));
		hateoas.put("selfLink", asBoolean(getNested(restConfig, "hateoas", "selfLink")));
		hateoas.put("updateLink", asBoolean(getNested(restConfig, "hateoas", "updateLink")));
		hateoas.put("deleteLink", asBoolean(getNested(restConfig, "hateoas", "deleteLink")));
		mapped.put("hateoas", hateoas);

		Map<String, Object> documentation = linkedMap();
		documentation.put("includeDefaultDocumentation", getNested(restConfig, "documentation", "includeDefaultDocumentation") != Boolean.FALSE);
		mapped.put("documentation", documentation);
		mapped.put("methods", methods);
		return mapped;
	}

	private List<Map<String, Object>> mapDtos(List<Object> dataObjects, boolean useJavaRecordsForDtos) {
		List<Map<String, Object>> result = new ArrayList<>();
		for (Object raw : dataObjects) {
			Map<String, Object> dataObject = asMap(raw);
			Map<String, Object> dto = linkedMap();
			dto.put("name", defaultIfBlank(trimmed(dataObject.get("name")), "DataObject"));
			dto.put("type", defaultIfBlank(trimmed(dataObject.get("dtoType")), "request"));
			List<Map<String, Object>> fields = new ArrayList<>();
			for (Object fieldRaw : asList(dataObject.get("fields"))) {
				fields.add(mapDtoField(asMap(fieldRaw)));
			}
			dto.put("fields", fields);
			if (!useJavaRecordsForDtos) {
				dto.put("classMethods", mapClassMethods(dataObject.get("classMethods")));
			}
			if (asBoolean(dataObject.get("mapperEnabled")) && !asList(dataObject.get("mapperModels")).isEmpty()) {
				Map<String, Object> mapper = linkedMap();
				mapper.put("enabled", true);
				mapper.put("models", asStringList(dataObject.get("mapperModels")));
				dto.put("mapper", mapper);
			}
			result.add(dto);
		}
		return result;
	}

	private List<Map<String, Object>> mapEnums(List<Object> enums) {
		List<Map<String, Object>> result = new ArrayList<>();
		for (Object raw : enums) {
			Map<String, Object> enumItem = asMap(raw);
			String name = trimmed(enumItem.get("name"));
			List<String> constants = asStringList(enumItem.get("constants"));
			if (name.isBlank() || constants.isEmpty()) {
				continue;
			}
			Map<String, Object> mapped = linkedMap();
			mapped.put("name", name);
			mapped.put("storage", defaultIfBlank(trimmed(enumItem.get("storage")), "STRING"));
			mapped.put("constants", constants);
			result.add(mapped);
		}
		return result;
	}

	private List<Map<String, Object>> mapMappers(List<Object> mappers) {
		List<Map<String, Object>> result = new ArrayList<>();
		for (Object raw : mappers) {
			Map<String, Object> mapper = asMap(raw);
			String name = trimmed(mapper.get("name"));
			String fromModel = trimmed(mapper.get("fromModel"));
			String toModel = trimmed(mapper.get("toModel"));
			List<Map<String, Object>> mappings = new ArrayList<>();
			for (Object mappingRaw : asList(mapper.get("mappings"))) {
				Map<String, Object> item = asMap(mappingRaw);
				String sourceField = trimmed(item.get("sourceField"));
				String targetField = trimmed(item.get("targetField"));
				if (sourceField.isBlank() || targetField.isBlank()) {
					continue;
				}
				Map<String, Object> mappedItem = linkedMap();
				mappedItem.put("sourceField", sourceField);
				mappedItem.put("targetField", targetField);
				mappings.add(mappedItem);
			}
			if (!name.isBlank() && !fromModel.isBlank() && !toModel.isBlank() && !mappings.isEmpty()) {
				Map<String, Object> mapped = linkedMap();
				mapped.put("name", name);
				mapped.put("fromModel", fromModel);
				mapped.put("toModel", toModel);
				mapped.put("mappings", mappings);
				result.add(mapped);
			}
		}
		return result;
	}

	private List<Map<String, Object>> mapAutoRequestMappers(List<Object> entities, List<Object> dataObjects) {
		Map<String, Map<String, Object>> dtoByName = new LinkedHashMap<>();
		for (Object dtoRaw : dataObjects) {
			Map<String, Object> dto = asMap(dtoRaw);
			String name = trimmed(dto.get("name"));
			if (!name.isBlank()) {
				dtoByName.put(name.toLowerCase(Locale.ROOT), dto);
			}
		}

		List<Map<String, Object>> autoMappers = new ArrayList<>();
		Set<String> seen = new LinkedHashSet<>();
		for (Object entityRaw : entities) {
			Map<String, Object> entity = asMap(entityRaw);
			String entityName = trimmed(entity.get("name"));
			if (entityName.isBlank() || !asBoolean(entity.get("addRestEndpoints")) || !asBoolean(getNested(entity, "restConfig", "mapToEntity"))) {
				continue;
			}
			Map<String, Object> restConfig = getNestedMap(entity, "restConfig");
			Map<String, Object> requestConfig = getNestedMap(restConfig, "requestResponse", "request");
			List<String> candidateDtos = List.of(
					extractDtoType(getNested(requestConfig, "create", "dtoName")),
					extractDtoType(getNested(requestConfig, "update", "dtoName")),
					extractDtoType(getNested(requestConfig, "patch", "dtoName")),
					extractDtoType(getNested(requestConfig, "list", "dtoName")),
					extractDtoType(requestConfig.get("bulkInsertType")),
					extractDtoType(requestConfig.get("bulkUpdateType")));
			for (String dtoName : new LinkedHashSet<>(candidateDtos)) {
				if (dtoName == null || dtoName.isBlank() || dtoName.equalsIgnoreCase(entityName)) {
					continue;
				}
				Map<String, Object> dto = dtoByName.get(dtoName.toLowerCase(Locale.ROOT));
				if (dto == null) {
					continue;
				}
				String mapperName = dtoName + "RequestMapper";
				String key = mapperName.toLowerCase(Locale.ROOT) + "::" + dtoName.toLowerCase(Locale.ROOT) + "::"
						+ entityName.toLowerCase(Locale.ROOT);
				if (!seen.add(key)) {
					continue;
				}
				Map<String, Object> mapper = linkedMap();
				mapper.put("name", mapperName);
				mapper.put("fromModel", dtoName);
				mapper.put("toModel", entityName);
				mapper.put("mappings", buildSameNameMappings(asList(dto.get("fields")), asList(entity.get("fields"))));
				autoMappers.add(mapper);
			}
		}
		return autoMappers;
	}

	private List<Map<String, Object>> mergeMapperSpecs(List<Map<String, Object>> manualMappers, List<Map<String, Object>> autoMappers) {
		List<Map<String, Object>> merged = new ArrayList<>();
		Set<String> seenNames = new LinkedHashSet<>();
		for (Map<String, Object> mapper : manualMappers) {
			String name = trimmed(mapper.get("name"));
			if (name.isBlank()) {
				continue;
			}
			seenNames.add(name.toLowerCase(Locale.ROOT));
			merged.add(mapper);
		}
		for (Map<String, Object> mapper : autoMappers) {
			String name = trimmed(mapper.get("name"));
			if (name.isBlank() || seenNames.contains(name.toLowerCase(Locale.ROOT))) {
				continue;
			}
			seenNames.add(name.toLowerCase(Locale.ROOT));
			merged.add(mapper);
		}
		return merged;
	}

	private String extractDtoType(Object rawType) {
		String value = trimmed(rawType);
		if (value.isBlank()) {
			return "";
		}
		if (value.startsWith("List<") && value.endsWith(">")) {
			return trimmed(value.substring(5, value.length() - 1));
		}
		return value;
	}

	private List<Map<String, Object>> buildSameNameMappings(List<Object> sourceFields, List<Object> targetFields) {
		Map<String, String> targetNamesByLower = new LinkedHashMap<>();
		for (Object fieldRaw : targetFields) {
			String name = trimmed(asMap(fieldRaw).get("name"));
			if (!name.isBlank()) {
				targetNamesByLower.put(name.toLowerCase(Locale.ROOT), name);
			}
		}
		List<Map<String, Object>> result = new ArrayList<>();
		for (Object fieldRaw : sourceFields) {
			String sourceName = trimmed(asMap(fieldRaw).get("name"));
			if (sourceName.isBlank()) {
				continue;
			}
			String targetName = targetNamesByLower.get(sourceName.toLowerCase(Locale.ROOT));
			if (targetName == null) {
				continue;
			}
			Map<String, Object> mapping = linkedMap();
			mapping.put("sourceField", sourceName);
			mapping.put("targetField", targetName);
			result.add(mapping);
		}
		return result;
	}

	private List<String> extractDependencies(String dependenciesString, List<String> selectedDependencies) {
		LinkedHashSet<String> all = new LinkedHashSet<>();
		for (String item : String.valueOf(dependenciesString == null ? "" : dependenciesString).split(",")) {
			String trimmedItem = item.trim();
			if (!trimmedItem.isEmpty()) {
				all.add(trimmedItem);
			}
		}
		for (String item : selectedDependencies) {
			String trimmedItem = item.trim();
			if (!trimmedItem.isEmpty()) {
				all.add(trimmedItem);
			}
		}
		return new ArrayList<>(all);
	}

	private List<String> mapProfiles(Object profiles) {
		if (!(profiles instanceof List<?> profileList)) {
			return new ArrayList<>();
		}
		LinkedHashSet<String> normalized = new LinkedHashSet<>();
		for (Object profile : profileList) {
			String normalizedProfile = normalizeProfileName(profile);
			if (normalizedProfile != null && isValidProfileName(normalizedProfile)) {
				normalized.add(normalizedProfile);
			}
		}
		return new ArrayList<>(normalized);
	}

	private List<String> getActuatorConfigurationOptions(Object profileSource) {
		List<String> normalizedProfiles = new ArrayList<>();
		for (String profile : mapProfiles(profileSource)) {
			if (!"default".equals(profile)) {
				normalizedProfiles.add(profile);
			}
		}
		List<String> result = new ArrayList<>();
		result.add("default");
		result.addAll(normalizedProfiles);
		return result;
	}

	private List<String> sanitizeActuatorEndpoints(Object rawEndpoints) {
		if (!(rawEndpoints instanceof List<?> endpoints)) {
			return new ArrayList<>(DEFAULT_ACTUATOR_ENDPOINTS);
		}
		LinkedHashSet<String> cleaned = new LinkedHashSet<>();
		for (Object endpoint : endpoints) {
			String item = String.valueOf(endpoint == null ? "" : endpoint).trim().toLowerCase(Locale.ROOT);
			if (!item.isEmpty() && ACTUATOR_ENDPOINT_OPTIONS.contains(item)) {
				cleaned.add(item);
			}
		}
		return cleaned.isEmpty() ? new ArrayList<>(DEFAULT_ACTUATOR_ENDPOINTS) : new ArrayList<>(cleaned);
	}

	private Map<String, List<String>> sanitizeActuatorConfigurations(Object rawConfigurations, List<String> availableConfigurations) {
		Set<String> allowedConfigurations = new LinkedHashSet<>(availableConfigurations);
		Map<String, List<String>> sanitized = new LinkedHashMap<>();
		sanitized.put("default", new ArrayList<>(DEFAULT_ACTUATOR_ENDPOINTS));
		if (rawConfigurations instanceof List<?>) {
			sanitized.put("default", sanitizeActuatorEndpoints(rawConfigurations));
			return sanitized;
		}
		if (!(rawConfigurations instanceof Map<?, ?> configurations)) {
			return sanitized;
		}
		for (Map.Entry<?, ?> entry : configurations.entrySet()) {
			String key = normalizeProfileName(entry.getKey());
			if (key == null || !allowedConfigurations.contains(key)) {
				continue;
			}
			Object value = entry.getValue();
			Object include = value instanceof Map<?, ?> valueMap
					? nonNull(getNested(asMap(valueMap), "endpoints", "include"), valueMap.get("include"))
					: value;
			sanitized.put(key, sanitizeActuatorEndpoints(include));
		}
		if (!sanitized.containsKey("default") || sanitized.get("default").isEmpty()) {
			sanitized.put("default", new ArrayList<>(DEFAULT_ACTUATOR_ENDPOINTS));
		}
		return sanitized;
	}

	private Map<String, Object> buildOperationResponseConfig(Map<String, Object> restConfig, String operationKey) {
		String responseType = "RESPONSE_ENTITY";
		String configuredResponseType = trimmed(getNested(restConfig, "requestResponse", "response", "responseType"));
		if ("DTO_DIRECT".equals(configuredResponseType)) {
			responseType = "DTO_DIRECT";
		} else if ("CUSTOM_WRAPPER".equals(configuredResponseType)) {
			responseType = "CUSTOM_WRAPPER";
		}
		String perOperationDto = trimmed(getNested(restConfig, "requestResponse", "response", "endpointDtos", operationKey));
		String defaultCustomWrapperDto = getDefaultCustomWrapperDto(restConfig, operationKey);

		Map<String, Object> response = linkedMap();
		response.put("responseType", responseType);
		if ("CUSTOM_WRAPPER".equals(responseType)) {
			response.put("dtoName", defaultIfBlank(perOperationDto,
					defaultIfBlank(trimmed(getNested(restConfig, "requestResponse", "response", "dtoName")), defaultCustomWrapperDto)));
		} else {
			response.put("dtoName", trimmed(getNested(restConfig, "requestResponse", "response", "dtoName")));
		}
		String responseWrapper = trimmed(getNested(restConfig, "requestResponse", "response", "responseWrapper"));
		if ("NONE".equals(responseWrapper)) {
			response.put("responseWrapper", "NONE");
		} else if ("UPSERT".equals(responseWrapper)) {
			response.put("responseWrapper", "UPSERT");
		} else {
			response.put("responseWrapper", "STANDARD_ENVELOPE");
		}
		response.put("enableFieldProjection", asBoolean(getNested(restConfig, "requestResponse", "response", "enableFieldProjection")));
		response.put("includeHateoasLinks", asBoolean(getNested(restConfig, "requestResponse", "response", "includeHateoasLinks")));
		return response;
	}


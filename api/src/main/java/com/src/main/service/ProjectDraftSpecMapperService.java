	private Map<String, Object> linkedMap() {
		return new LinkedHashMap<>();
	}

	private Map<String, Object> mapOf(Object... items) {
		Map<String, Object> result = linkedMap();
		for (int i = 0; i + 1 < items.length; i += 2) {
			result.put(String.valueOf(items[i]), items[i + 1]);
		}
		return result;
	}

	private Object getNested(Map<String, Object> map, Object... keys) {
		Object current = map;
		for (Object key : keys) {
			if (!(current instanceof Map<?, ?> currentMap)) {
				return null;
			}
			current = currentMap.get(key);
		}
		return current;
	}

	private Map<String, Object> getNestedMap(Map<String, Object> map, Object... keys) {
		return asMap(getNested(map, keys));
	}

	private List<Object> getNestedList(Map<String, Object> map, Object... keys) {
		return asList(getNested(map, keys));
	}

	@SuppressWarnings("unchecked")
	private Map<String, Object> asMap(Object value) {
		if (value instanceof Map<?, ?> map) {
			Map<String, Object> result = new LinkedHashMap<>();
			for (Map.Entry<?, ?> entry : map.entrySet()) {
				result.put(String.valueOf(entry.getKey()), entry.getValue());
			}
			return result;
		}
		return new LinkedHashMap<>();
	}

	private List<Object> asList(Object value) {
		if (value instanceof List<?> list) {
			return new ArrayList<>(list);
		}
		return new ArrayList<>();
	}

	private List<String> asStringList(Object value) {
		LinkedHashSet<String> result = new LinkedHashSet<>();
		for (Object item : asList(value)) {
			String text = trimmed(item);
			if (!text.isBlank()) {
				result.add(text);
			}
		}
		return new ArrayList<>(result);
	}

	private String trimmed(Object value) {
		return value == null ? "" : String.valueOf(value).trim();
	}

	private boolean hasNumber(Object value) {
		if (value == null) {
			return false;
		}
		try {
			Double.parseDouble(String.valueOf(value));
			return true;
		} catch (NumberFormatException ex) {
			return false;
		}
	}

	private Object toNumberIfPossible(String value) {
		try {
			double numeric = Double.parseDouble(value);
			if (numeric == Math.rint(numeric)) {
				long asLong = (long) numeric;
				if (asLong <= Integer.MAX_VALUE && asLong >= Integer.MIN_VALUE) {
					return (int) asLong;
				}
				return asLong;
			}
			return numeric;
		} catch (NumberFormatException ex) {
			return value;
		}
	}

	private Object toBooleanIfPossible(String value) {
		String normalized = value.toLowerCase(Locale.ROOT);
		if ("true".equals(normalized)) {
			return true;
		}
		if ("false".equals(normalized)) {
			return false;
		}
		return value;
	}

	private String normalizeType(Object type) {
		String value = trimmed(type);
		Map<String, String> typeMap = Map.of(
				"Int", "Integer",
				"Decimal", "BigDecimal",
				"Date", "LocalDate",
				"Time", "LocalTime",
				"DateTime", "OffsetDateTime",
				"Json", "String");
		return typeMap.getOrDefault(value, value.isBlank() ? "String" : value);
	}

	private String toSnakeCase(String value) {
		return value
				.replaceAll("([a-z0-9])([A-Z])", "$1_$2")
				.replaceAll("[\\s\\-]+", "_")
				.replaceAll("__+", "_")
				.toLowerCase(Locale.ROOT);
	}

	private String toArtifactId(String value) {
		return value.trim()
				.replaceAll("([a-z0-9])([A-Z])", "$1-$2")
				.replaceAll("[\\s_]+", "-")
				.replaceAll("-+", "-")
				.toLowerCase(Locale.ROOT);
	}

	private String toDatabaseCode(Object value) {
		String raw = trimmed(value);
		String normalized = raw.toLowerCase(Locale.ROOT);
		List<String> known = Arrays.asList("NONE", "OTHER", "MSSQL", "MYSQL", "MARIADB", "ORACLE", "POSTGRES", "MONGODB", "DERBY", "H2", "HSQL");
		if (known.contains(raw.toUpperCase(Locale.ROOT))) {
			return raw.toUpperCase(Locale.ROOT);
		}
		return switch (normalized) {
			case "none" -> "NONE";
			case "other" -> "OTHER";
			case "mssql", "mssql server", "sql server" -> "MSSQL";
			case "mysql" -> "MYSQL";
			case "mariadb" -> "MARIADB";
			case "oracle" -> "ORACLE";
			case "postgres", "postgresql" -> "POSTGRES";
			case "mongodb", "mongo" -> "MONGODB";
			case "derby", "apache derby" -> "DERBY";
			case "h2", "h2 database" -> "H2";
			case "hsql", "hypersql", "hsql database" -> "HSQL";
			default -> "POSTGRES";
		};
	}

	private String resolveDatabaseType(Object type, Object databaseCode) {
		String normalizedType = trimmed(type).toUpperCase(Locale.ROOT);
		if ("NONE".equals(normalizedType)) {
			return "NONE";
		}
		if ("NOSQL".equals(normalizedType)) {
			return "NOSQL";
		}
		if ("SQL".equals(normalizedType)) {
			return "SQL";
		}
		String normalizedDb = toDatabaseCode(databaseCode);
		if ("NONE".equals(normalizedDb)) {
			return "NONE";
		}
		if ("MONGODB".equals(normalizedDb)) {
			return "NOSQL";
		}
		return "SQL";
	}

	private String normalizeProfileName(Object value) {
		if (value == null) {
			return null;
		}
		String text = String.valueOf(value).trim();
		return text.isEmpty() ? null : text.toLowerCase(Locale.ROOT);
	}

